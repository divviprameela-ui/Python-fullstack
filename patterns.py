#patterns
#right angle triangle

n = int(input("Enter number of rows: "))
for i in range(1, n + 1):
    print("*" * i)
#reverse tringle
    
n = int(input("Enter number of rows: "))
for i in range(n, 0, -1):
    print("*" * i)

#square
n = int(input("Enter number of rows: "))
for i in range(n):
    print("*" * n)
